			<h2 style="center">The Temple Complex</h2>
			<p class="about-para-w3ls">The Ambadevi temple complex is surrounded with high walls and on entering the compound, one can find a huge spacious hall. The left side leads to the courtyard through a number of corridors. The main gates are huge with the look of a fort gate. Above the gate, there is a small chamber also known as the “Nagarkhana” or the drum room. The temple walls are full with pictorial depiction of the Puranas.
The Ambadevi Temple
The main “Garbagriha” or the “Gabharas” is broken into three large arch shaped rooms with sandstone idols of Goddess Ambabai, Lord Ganapati, Lord Mahadev, Lord Vishnu and Goddess Parvati. The central chamber is where Goddess Amba resides with Lakshmi and Narayan. Two silver lamps are seen burning in front of the deities for 24 hours and 365 days. The western wall of the temple complex has idols and engravings of various Gods and Goddesses. The age of these idols are uncertain. They are engraved with such detail that they almost look like machine cut. It is still a mystery about the creation of the temple plaster work and ornaments of the deities.
