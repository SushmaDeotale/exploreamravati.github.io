			<h2 style="center">Climate in Chikhaldara</h2>
			<p class="about-para-w3ls">June, July, August, and September are the four months of Monsoon. Chikhaldara experiences heavy rainfall during these months. The average temperature during monsoon is twenty-five degree celsius and during winter it goes below 15°C.  Summer is not pleasant in this place because the temperature is very high . It is good not to visit Chikhaldara during summer because the temperature here lies between 32°C to 40°C.[2]

According to Köppen climate classification, climate of Chikhaldara is categorized as Humid Subtropical, Cwa. Chikhaldara has four distinct seasons - Winter, Summer (Late winter and early summer can be called spring but is of very short duration), Monsoon (Rainy season) and Post-monsoon or Autumn. Summers are hot and long, from mid-March to Early June. Summer temperature varies from 16°C to 41°C. Monsoon starts in early June and lasts till early October. July and October are wettest months. Chikhaldara receives averagely 1600 mm of rainfall annually. Mist following morning rain is common. Monsoon temperature ranges from 18°C to 26°C. Post-Monsoon starts mid-October and lasts till mid-November. Autumn, which is a transition between Post-Monsoon and Winter, is of short duration and lasts from mid-November to mid-December. Temperature during post monsoon and autumn ranges from 12°C to 30°C. Winters of Chikhaldara are mild with chilly, foggy mornings; pleasant, clear and sunny days; hazy and calm evenings and hazy, cool nights. Temperature in winter ranges from 4°C to 23°C.</p></div>
