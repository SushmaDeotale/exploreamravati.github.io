			<p class="about-para-w3ls">Saint Tukdoji Maharaj was born in Yawali, Maharashtra. He received spiritual initiation from Samarth Adkoji Maharaj of Warkhed gram. Early in childhood, Saint Tukdoji Maharaj performed rigorous penance and spiritual exercises in self-realisation. He also was a great orator and a musician who composed more than 3000 bhajans (spiritual poems) in Hindi and Marathi, having performed for the spiritual teacher Meher Baba in 1937[1] and 1944.[2] He has also written many articles on Dharma, society, nation and education.

